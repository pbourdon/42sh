#include "libft.h"

char		*ft_strdup(const char *s1)
{
	int		i;
	int		len;
	char	*dst;

	len = ft_strlen(s1);
	dst = ft_memalloc(sizeof(*dst) * (len + 1));
	i = 0;
	while (i < len)
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
